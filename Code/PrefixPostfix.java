public class PrefixPostfix {
   public static void main(String[] args) {
   	// Write Java code here
   	System.out.print("b--");
   	System.out.print("\n");
   
   	int b = 5;
   	while ((b--) != 0)
   	{
   		System.out.print(b);
   		System.out.print("\n");
   	}
   
   	b = 5;
   	System.out.print("--b");
   	System.out.print("\n");
   	while ((--b) != 0)
   	{
   		System.out.print(b);
   		System.out.print("\n");
	}

 }
}